    private void loadProfile() {
    try {
        Connection conn = DBConnection.getConnection();
        String sql = "SELECT username, job, email, gender, phone_number, location FROM user_accounts WHERE id = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, sessions.userId);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            jLabel3.setText(rs.getString("username"));       // Name
            jLabel5.setText(rs.getString("job"));            // Job
            jLabel7.setText(rs.getString("email"));          // Email
            jLabel10.setText(rs.getString("gender"));         // Gender
            jLabel14.setText(rs.getString("phone_number"));  // Phone Number
            jLabel12.setText(rs.getString("location"));      // Location
        }

        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "‚ùå Error fetching profile: " + e.getMessage());
    }
}

