   public void loadData() {
    String sql = "SELECT email_address, file_name, sent_date FROM email_history WHERE user_id = ? ORDER BY sent_date DESC";

    try {
        Connection conn = DBConnection.getConnection();
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, sessions.userId);  // Use the session's userId
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            String email = rs.getString("email_address");
            String fileName = rs.getString("file_name");
            Timestamp sentDate = rs.getTimestamp("sent_date");

            model.addRow(new Object[]{email, fileName, sentDate});
        }

        rs.close();
        pst.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading user history: " + e.getMessage());
    }
}
